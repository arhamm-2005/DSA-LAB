
package javaapplication23;

public class JavaApplication23 {


    public static void main(String[] args) {
        double primDouble = 25.75;
        
        // Converting primitive double to Wrapper Double
        Double wrappedDouble = primDouble;
        
        System.out.println("Primitive Double: " + primDouble);
        System.out.println("Wrapper Double: " + wrappedDouble);
    }
    
}
